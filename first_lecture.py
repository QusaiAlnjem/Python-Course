""" Arithmetic Operations """
# +	Addition	x + y	
# -	Subtraction	x - y	
# *	Multiplication	x * y	
# /	Division	x / y	
# %	Modulus	x % y	
# **	Exponentiation	x ** y	
# //	Floor division	x // y
x = 20
y = 3
print("+	Addition:" , x + y)	
print("-	Subtraction:" , x - y)
print("*	Multiplication:" , x * y)
print("/	Division:" , x / y)
print("%	Modulus:" , x % y)
print("**	Exponentiation:" , x ** y)	
